# Pedirle un número al usuario (por consola)
number = input('Dame un número, capitán!: ')

# Convirtiendo el número a int y lo multiplico por 2
result_int = int(number) * 2

# Convirtiendo el número a float y lo multiplico por 2
result_float = float(number) * 2

#? Es importante tener en cuenta el tipo de dato que estamos trabajando o nos dará errores

# Mostrando el resultado
print(result_float)